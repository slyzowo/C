#include <stdio.h>
int main(){

// Letters
  char a = 'A';              // single character                %c (character)
  char b[] = "BBB";          // array of characters             %s (string)

// Numbers
  float c = 3.14159;         // 4 bytes of memory 6-7 digits    %f  (float)
  double e = 3.14159265359;  // 8 bytes of memory 15-16 digits  %lf (long float)

// True or False
  bool f = true;             // 1 byte True or False            %d

// 

return 0;
}